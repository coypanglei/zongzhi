@ActivityScope
public class MyWorkLinePresenter extends ReworkBasePresenter<MyWorkLineContract.Model, MyWorkLineContract.View> {
    @Inject
    RxErrorHandler mErrorHandler;
    @Inject
    Application mApplication;
    @Inject
    ImageLoader mImageLoader;
    @Inject
    AppManager mAppManager;

    @Inject
    public MyWorkLinePresenter(MyWorkLineContract.Model model, MyWorkLineContract.View rootView) {
        super(model, rootView);
    }

    @Override
    public void onDestroy() {
        super.onDestroy();
        this.mErrorHandler = null;
        this.mAppManager = null;
        this.mImageLoader = null;
        this.mApplication = null;
    }

    public void getLatLng() {
        commonGetData(mModel.getLatLng(), mErrorHandler, itemBean -> {
            mRootView.setLatLng(itemBean);
        });

    }
}
