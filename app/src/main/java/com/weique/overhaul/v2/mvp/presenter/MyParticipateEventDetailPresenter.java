@ActivityScope
public class MyParticipateEventDetailPresenter extends BasePresenter<MyParticipateEventDetailContract.Model, MyParticipateEventDetailContract.View> {
    @Inject
    RxErrorHandler mErrorHandler;
    @Inject
    Application mApplication;
    @Inject
    ImageLoader mImageLoader;
    @Inject
    AppManager mAppManager;

    @Inject
    public MyParticipateEventDetailPresenter(MyParticipateEventDetailContract.Model model, MyParticipateEventDetailContract.View rootView) {
        super(model, rootView);
    }

    @Override
    public void onDestroy() {
        super.onDestroy();
        this.mErrorHandler = null;
        this.mAppManager = null;
        this.mImageLoader = null;
        this.mApplication = null;
    }
}
