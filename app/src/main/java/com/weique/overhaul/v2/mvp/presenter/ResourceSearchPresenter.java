@ActivityScope
public class ResourceSearchPresenter extends ReworkBasePresenter<ResourceSearchContract.Model, ResourceSearchContract.View> {
    @Inject
    RxErrorHandler mErrorHandler;
    @Inject
    Application mApplication;
    @Inject
    ImageLoader mImageLoader;
    @Inject
    AppManager mAppManager;

    @Inject
    public ResourceSearchPresenter(ResourceSearchContract.Model model, ResourceSearchContract.View rootView) {
        super(model, rootView);
    }

    @Override
    public void onDestroy() {
        super.onDestroy();
        this.mErrorHandler = null;
        this.mAppManager = null;
        this.mImageLoader = null;
        this.mApplication = null;
    }

    public void getResourceSearchListData(boolean pullToRefresh, boolean isLoadMore) {
        handlePaging(pullToRefresh, isLoadMore);
        commonGetData(mModel.getResourceSearchListData(pageSize,ignoreNumber), mErrorHandler, itemBean -> {
            try {
                mRootView.setAnswerListData(itemBean,isLoadMore);
                handlePaginLoadMore((itemBean == null || itemBean.getInterViewRecord() == null) ? 0 : itemBean.getInterViewRecord().size());
            } catch (Exception e) {
                e.printStackTrace();
            }
        });
    }
}
