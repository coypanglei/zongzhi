@ActivityScope
public class MyWorkLineModel extends BaseModel implements MyWorkLineContract.Model {
    @Inject
    Gson mGson;
    @Inject
    Application mApplication;

    @Inject
    public MyWorkLineModel(IRepositoryManager repositoryManager) {
        super(repositoryManager);
    }

    @Override
    public void onDestroy() {
        super.onDestroy();
        this.mGson = null;
        this.mApplication = null;
    }

    @Override
    public Observable<BaseBean<List<WorkLineLatBean>>> getLatLng() {
        return mRepositoryManager.obtainRetrofitService(ContradictionService.class).getLatLng(SignUtil.paramSign(null));
    }

}