@ActivityScope
public class AnswerDetailPresenter extends ReworkBasePresenter<AnswerDetailContract.Model, AnswerDetailContract.View> {
    @Inject
    RxErrorHandler mErrorHandler;
    @Inject
    Application mApplication;
    @Inject
    ImageLoader mImageLoader;
    @Inject
    AppManager mAppManager;

    @Inject
    public AnswerDetailPresenter(AnswerDetailContract.Model model, AnswerDetailContract.View rootView) {
        super(model, rootView);
    }

    @Override
    public void onDestroy() {
        super.onDestroy();
        this.mErrorHandler = null;
        this.mAppManager = null;
        this.mImageLoader = null;
        this.mApplication = null;
    }

    public void getPartyAnswerDetailData(boolean pullToRefresh, boolean isLoadMore, String SubjectId) {
        commonGetData(mModel.getPartyAnswerDetailData(SubjectId), mErrorHandler, questionDetailAnswerItemBeans ->
                mRootView.setAnswerDetailListData(questionDetailAnswerItemBeans));
    }
}
