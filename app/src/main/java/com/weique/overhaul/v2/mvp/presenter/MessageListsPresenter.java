@FragmentScope
public class MessageListsPresenter extends ReworkBasePresenter<MessageListsContract.Model, MessageListsContract.View> {
    @Inject
    RxErrorHandler mErrorHandler;
    @Inject
    Application mApplication;
    @Inject
    ImageLoader mImageLoader;
    @Inject
    AppManager mAppManager;

    @Inject
    public MessageListsPresenter(MessageListsContract.Model model, MessageListsContract.View rootView) {
        super(model, rootView);
    }

    @Override
    public void onDestroy() {
        super.onDestroy();
        this.mErrorHandler = null;
        this.mAppManager = null;
        this.mImageLoader = null;
        this.mApplication = null;
    }

    public void getMessage(boolean pullToRefresh, boolean isLoadMore) {
        handlePaging(pullToRefresh, isLoadMore);
        commonGetData(mModel.getMessage(pageSize, ignoreNumber), mErrorHandler, messageListBean -> {
            mRootView.setNewData(messageListBean.getList(), isLoadMore);
            handlePaginLoadMore((messageListBean == null || messageListBean.getList() == null) ? 0 : messageListBean.getList().size());
        });

    }
}
