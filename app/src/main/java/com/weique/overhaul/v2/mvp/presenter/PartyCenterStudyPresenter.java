@ActivityScope
public class PartyCenterStudyPresenter extends ReworkBasePresenter<PartyCenterStudyContract.Model, PartyCenterStudyContract.View> {
    @Inject
    RxErrorHandler mErrorHandler;
    @Inject
    Application mApplication;
    @Inject
    ImageLoader mImageLoader;
    @Inject
    AppManager mAppManager;

    @Inject
    public PartyCenterStudyPresenter(PartyCenterStudyContract.Model model, PartyCenterStudyContract.View rootView) {
        super(model, rootView);
    }

    @Override
    public void onDestroy() {
        super.onDestroy();
        this.mErrorHandler = null;
        this.mAppManager = null;
        this.mImageLoader = null;
        this.mApplication = null;
    }

    public void getPartyAnswerStudyData( String SubjectId) {
        commonGetData(mModel.getPartyAnswerStudyData(SubjectId),
                mErrorHandler, questionDetailAnswerItemBeans ->{
                    try {
                        mRootView.setAnswerStudyData(questionDetailAnswerItemBeans);
                    } catch (Exception e) {
                        e.printStackTrace();
                    }
                } );
    }
}
