@FragmentScope
public class QuestionDetailPresenter extends ReworkBasePresenter<QuestionDetailContract.Model, QuestionDetailContract.View> {
    @Inject
    RxErrorHandler mErrorHandler;
    @Inject
    Application mApplication;
    @Inject
    ImageLoader mImageLoader;
    @Inject
    AppManager mAppManager;

    @Inject
    public QuestionDetailPresenter(QuestionDetailContract.Model model, QuestionDetailContract.View rootView) {
        super(model, rootView);
    }

    @Override
    public void onDestroy() {
        super.onDestroy();
        this.mErrorHandler = null;
        this.mAppManager = null;
        this.mImageLoader = null;
        this.mApplication = null;
    }

    public void getPartyAnswer(String Subjectid,  String Answer, String SubjectDetailId) {
        commonGetData(mModel.getPartyAnswer(Subjectid,Answer, SubjectDetailId), mErrorHandler, integralBean -> mRootView.getFragment().setData(integralBean));
    }
}
