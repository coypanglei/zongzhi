@ActivityScope
public class PatrolsDetailModel extends BaseModel implements PatrolsDetailContract.Model {
    @Inject
    Gson mGson;
    @Inject
    Application mApplication;

    @Inject
    public PatrolsDetailModel(IRepositoryManager repositoryManager) {
        super(repositoryManager);
    }

    @Override
    public void onDestroy() {
        super.onDestroy();
        this.mGson = null;
        this.mApplication = null;
    }

    @Override
    public Observable<BaseBean<PatrolsDetailItemBean>> getPatrolsDetailListData(int pageSize, int ignoreNumber, String id) {
        Map<String, Object> map = new HashMap<>(8);
        map.put("PageSize", pageSize);
        map.put("IgnoreNumber", ignoreNumber);
        map.put("id", id);
        return mRepositoryManager
                .obtainRetrofitService(ResouceSearchService.class)
                .getPatrolsDetailListData(SignUtil.paramSign(map));
    }

    @Override
    public Observable<BaseBean<GridInformationBean>> getGetDepartment() {
        Map<String, Object> map = new HashMap<>(8);
        return mRepositoryManager.obtainRetrofitService(InformationService.class).getDepartment(SignUtil.paramSign(map));
    }
}